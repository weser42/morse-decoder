const MORSE_TABLE = {
  ".-": "a",
  "-...": "b",
  "-.-.": "c",
  "-..": "d",
  ".": "e",
  "..-.": "f",
  "--.": "g",
  "....": "h",
  "..": "i",
  ".---": "j",
  "-.-": "k",
  ".-..": "l",
  "--": "m",
  "-.": "n",
  "---": "o",
  ".--.": "p",
  "--.-": "q",
  ".-.": "r",
  "...": "s",
  "-": "t",
  "..-": "u",
  "...-": "v",
  ".--": "w",
  "-..-": "x",
  "-.--": "y",
  "--..": "z",
  ".----": "1",
  "..---": "2",
  "...--": "3",
  "....-": "4",
  ".....": "5",
  "-....": "6",
  "--...": "7",
  "---..": "8",
  "----.": "9",
  "-----": "0",
};

function decode(expr) {
  let a = "";
  let d = "";
  let e = "";
  for (let i = 0; i < expr.length; i += 10) {
    a = expr.slice(i, i + 10);
    let b = "";
    let c = "";
    for (let j = 0; j < a.length; j += 2) {
      c = a[j] + a[j + 1];
      if (c != 10 && c != 11) {
        b += "";
      } else if (c == "10") {
        b += ".";
      } else {
        b += "-";
      }
    }
    d = MORSE_TABLE[b];
    if (!d) {
      e += " ";
    } else {
      e += d;
    }
  }
  return e;
}

